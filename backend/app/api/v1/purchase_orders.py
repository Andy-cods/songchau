"""Purchase Orders API — list, create, update, submit, receive."""

from __future__ import annotations

from datetime import date
from fastapi import APIRouter, Depends, HTTPException, Query
from pydantic import BaseModel
import asyncpg

from app.core.database import get_db
from app.core.rbac import require_role
from app.core.security import TokenData
from app.services.workflow_engine import (
    create_workflow,
    execute_action,
    WFAction,
)

router = APIRouter()


# ---------------------------------------------------------------------------
# Schemas
# ---------------------------------------------------------------------------

class POLineItem(BaseModel):
    product_id: str
    quantity: float
    unit: str = "cái"
    unit_price: float
    note: str | None = None


class POCreateRequest(BaseModel):
    supplier_id: str
    expected_date: date | None = None
    notes: str | None = None
    line_items: list[POLineItem]


class POUpdateRequest(BaseModel):
    supplier_id: str | None = None
    expected_date: date | None = None
    notes: str | None = None
    line_items: list[POLineItem] | None = None


class ReceiveLineItem(BaseModel):
    po_line_id: str
    quantity_received: float
    note: str | None = None


class POReceiveRequest(BaseModel):
    received_items: list[ReceiveLineItem]


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

async def _get_po(conn: asyncpg.Connection, po_id: str) -> dict:
    row = await conn.fetchrow(
        "SELECT * FROM purchase_orders WHERE id = $1", po_id
    )
    if not row:
        raise HTTPException(status_code=404, detail="Đơn mua hàng không tồn tại")
    return dict(row)


async def _get_po_lines(conn: asyncpg.Connection, po_id: str) -> list[dict]:
    rows = await conn.fetch(
        """
        SELECT pol.*, p.product_name, p.bqms_code
        FROM po_line_items pol
        LEFT JOIN products p ON p.id = pol.product_id
        WHERE pol.po_id = $1
        ORDER BY pol.line_number ASC
        """,
        po_id,
    )
    return [dict(r) for r in rows]


